        <h2>28. Lambadas Tribe</h2>
        <p>
        The Lambadas are a scheduled tribe inhabiting throughout of Andhra Pradesh.
        The Lambadas are the largest tribe in Andhra Pradesh. Lambadas live in exclusive settlements of their own called Tandas, usually away from the main village, tenaciously maintaining their cultural and ethnic identity.
        The traditional dress of the men comprises of dhoti, upper garment and bright coloured turbans. The womenfolk wear langa' of coarse cotton prints, richly embroidered with several fold at the waists.
        Lambada tribe is divided into five phratries viz.,
        1. Bhukya (Rathod)
        2. Vadthiya (Jadhav)
        3. Chowhan
        4. Pamar
        5. Banoth (Ade)
        These phratries are further divided into a number of patrilineal kin groups called Pada or Jath (Clan) in their dialect. Bhukya Phratry consists of 27 clans, Vadthiya 52, Chowhan 6 Pamar 12 and Banoth 13.
