        <h2>15. Kondhs_and_Kodi Tribe</h2>
        <p>Khonds are chiefly residing in the densely wooded hill slopes in the scheduled areas of Srikakulam, Vizianagaram and Visakhapatnam districts of Andhra Pradesh. They are also known as Samantha, Konda Kodu, Jatapu, Jatapu Dora, Kodi, Kodhu, Kondu and Kuinga. These terms are used for Khonds in different areas of Srikakulam, Vizianagaram and Visakhapatnam districts. The Khonds call themselves in their own dialect as Kuinga or Kui Dora. <br> The Khonds are divided into the following Sub-tribes. <br> 1. Dongria Khond, 2. Desya Khond, 3. Kuttiya Khond, 4. Tikiria Khond, 5. Yeneti Khond.
