        <h2>26. Rona and Rena Tribe</h2>
        <p>
        They are also known as Rena and Rana, which mean "battle". They are found living in the agency areas of Visakhapatnam and Vizianagaram districts of Andhra Pradesh.
        Cross-cousin marriages are common and levirate and sororate are allowed in the community. Elopement and negotiation are the modes of acquiring spouses. Bride price is paid in kind. Remarriage is allowed for widow and divorcee. Nuclear families are common. They follow the rule of patrilineal decent and patrilocal residence. Divorce is permissible.
        Main occupation of Rona is settled agriculture. They work as wage labourers. Their traditional community council is headed by a Naidu. They worship village gods like Pedda Devudu, Nandi Devudu, Ganga Devudu, Jatra Devudu and ancestral spirits. They celebrate the festivals of Itukala Panduga, Korra Kotha, Jodla Panduga besides Hindu festivals like Sivarathri, Ugadi and Diwali.
