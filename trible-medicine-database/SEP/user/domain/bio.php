        <h2>29. Thoti Tribe</h2>
        <p>
        Thotis living in the districts of Adilabad, Hyderabad, Karim Nagar, Khammam, Mahaboob Nagar, Medak, Nalgonda, Nizamabad and Warangal in Andhra Pradesh are listed as Scheduled Tribes. 
        Thotis are recognized as Primitive Tribal Group Thoti tribe is divided into four exogamous phratries just as Saga of Gonds. All the phratries or Sagas are sub divided into exogamous clans, Among Thotis clan name precedes the personal name and is treated as surname. Every clan is strictly exogamous. Monogamy is generally practised by Thotis. Widow marriages are permissible.
        The mother tongue of Thotis is Gondi. Gonds call the Thotis as Birdal (receiver) and latter cell the former as Dhani (Donar). The traditional occupation of Thotis is acting as bards to their Gond patrons and women practice tattooing, but presently majority of Thotis are earning their livelihood as agricultural and casual labourers.
        The political organization at the village level among Thoti tribe is known as Panch. It consists of Patla, Mahajan, Devari, Ghattiyal and Havaldar.
