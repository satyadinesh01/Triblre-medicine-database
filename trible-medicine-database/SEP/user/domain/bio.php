        <h2>24. Porja Tribe</h2>
        <p>
        Porjas are found predominantly in the tribal areas of Visakhapatnam district of Andhra Pradesh.
        They are recognized as Primitive Tribal Group. They have their own dialect. In addition to their own dialect, they speak Telugu as well as Adivasi Oriya.
        Porja tribe is divided into following endogamous sub-divisions or sub-groups.
        1) Parangi Porja                
        2) Jhodia Porja
        3) Gadaba Porja
        4) Banang Porja
        5) Pangu Porja
        6) Kollai Porja
        7) Didoi Porja.
        Each endogamous sub-group is further divided into following totemic clans which are popularly known as "bowau' in local pariance.
        1) Killo (tiger)
        2) Kimudu (Bear)
        3) Korra (millet)
        4) Rambi (bird)
        5) Pangi (kite)
        6) Ontelu (snake)
        7) Gollori (monkey)
        Clan names are pre-fixed to the individual names. The woman gets the clan name of the husband after marriage.
