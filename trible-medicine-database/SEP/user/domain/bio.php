        <h2>16. Kotia Tribe</h2>
        <p>Kotia tribe is divided into the following sub divisions or sub groups (1) Bodo Kotia, (2) Sano Kotia, (3) Putia Poika and (4) Dhulia. In Visakhapatnam agency, Bodo Kotias are also called Doras and claiming equal status with Bagata, a tribe with higher social status in Visakhapatnam district. Bodo Kotia people do not accept cooked food from Sano Kotia people as they are considered inferior in social status. Similarly Sano Kotia people also do not accept food from those of Putia Poika. Kotia tribe is divided into various totemic clans and each clan is further divided into different surnames. Some of the clan names are Matya (Fish), Naga (Snake), Geedh (Eagle), Gorapitta (a kind of bird) etc. All the sub divisions of Kotia community speak corrupt form of Oriya.
