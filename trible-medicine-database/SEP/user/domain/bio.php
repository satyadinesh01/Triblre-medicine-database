        <div class="matter">
        <h2>31. Yenadis Tribe</h2>
        <p>
        Yanadis are one of the major scheduled tribes of Andhra Pradesh. Thurston (1909) noted that the people were natives of Sriharikota iland and suggested that they derived their name from the Sanskrit word "anadi" denoting those whose origin is unknown. Now they are predominantly spread over the districts of Nellore, Chittoor and Prakasham and are concentrated in the Nellore district. Yanadis live in symbiosis with non-tribals.
        Their mother tongue is Telugu. Yanadis are broadly divided into four endogamous groups on the basis of occupations and dietary habits. The sub divisions are:
        1. Manchi Yanadis or Reddi Yanadis (Cultivators and servants)
        2. Adivi Yanadis (those living in forests)
        3. Paki Yanadis (Scavengers) and
        4. Challa Yanadis (those who collect left out food from leaf plates in the dust bins).
        The challa Yanadis and Paki Yanadis are considered to be unclear and low among Yanadis. Each division of the Yanadi is further divided into a number of patrilineal exogamous groups representing their lineage names (Intiperlu).
        Marriage by negotiations, by mutual love and elopement are usual modes of acquiring mates. The re-marriage of divorcees, widows and widowers is permitted. The nuclear type of family is more predominant.
        They propitiate some village deities like Peisramme Chenchemma.
        </p>
