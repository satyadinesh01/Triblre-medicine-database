        <h2>23. Pardhan Tribe</h2>
        <p>
        Pardhans are inhabiting the tribal areas of Adilabad district. Pardhans or Pradhans are traditional bards to Gonds and recite mythologies, folk tales and songs of their gods and goddesses at various festivals, ceremonies and fairs for which service they are paid in cash or kind. This patron-client relationship comes from generation to generation. Gonds call them 'Patadi' meaning singer or genealogist. 
        Pardhan is 33.63. Pardhans mother tongue is Marati. But they recite Gonds' mythologies and folk tales in Gondi. Pardhan community is divided into four phratries and they possess similar clans of Gonds. The basic rule of the patron-client relationship of Gonds and Pardhans is that both of them should belong to the same phratry and same cian. The four phratries in Pardhan tribe are Satha Devi (Seven deity group), Saha Devi (Six deity group) Pacha Devi (Five deity group) and Chow Devi (Four deitygroup). Each phratry is further divided into exogamous clans similar to Gonds.
