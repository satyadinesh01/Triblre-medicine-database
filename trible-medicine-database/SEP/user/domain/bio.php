        <h2>18. Kulia Tribe</h2>
        <p>Kulia is numerically very small tribe inhabiting the tribal areas of Visakhaptnam district. Their settlements are confined to the wooded tracts of Araku, Paderu, Pedabayalu and Munchingput mandals of Visakhapatnam district. They are also called Mulias. 
Kulias are divided into number of exogamous patrilineal clans. The major clans are 1) Naga, 2) Surjo, 3) Matya, 4) Killo, 5) Hanuman or Golleri and 6) Pangi. The institution of Nestam (bond friendship), which is also called Goth Band Bar, is in vogue.
Kulias observe clan exogamy. Though marriage by negotiations is the most common form of marriage, marriage by capture and marriage by elopement are also in practice. Polygyny is also in vogue. Both levirate and sororate are permitted
They speak Oriya among themselves, but are equally proficient in Telugu. They celebrate Korra-sama Kotha, Metta, Dhanyam Kotha, Chikkudu Kotha and Mamidi Kotha festivals.
Their traditional occupations are agriculture and selling bangles, beeds and trinkets They also collect minor forest produce and sell in the weekly shandies.</p>
