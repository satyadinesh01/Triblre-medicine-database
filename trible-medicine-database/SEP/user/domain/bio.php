        <h2>32. Yerukulas Tribe</h2>
        <p>
        Yerukala is scheduled tribe found throughout Andhra Pradesh. They call themselves 'Kurru'. They are called 'Yerukula' after their women's traditional profession of fortune telling ((Eruka chepputa)
        The Yerukala tribe has a dialect of its own which is called Yerukula basha' or 'Kurru basha' or 'Kulavatha'. It is derived from Dravidian languages, mostly Telugu, Tamil and Kannada.
        The Yerukala tribe is divided into a number of functional and endogamous sub- divisions and each such sub-division is named after the commodity, which they traded in and the occupation they adopted. The sub-divisions are Dabba Yerukala (those who make baskets from split bamboo), Yeethapullala (Date twigs) Yerukula (those whom makle baskets from wild date leaves), Kunchapuri Yerukala (those who make weaver's combs), Parikamuggula Yerukala (sooth sayers and beggars), Karivepaku (curry leaves) Yerukala (hawkers of curry leaves), Uppu (salt) Yerukala (salt hawkers).
        Each sub-division is dividend into four phraties viz., Sathupadi, Kavadi, Manupati and Mendraguthi. The first two phratiries Viz, Sathupadi and Kavadi are considered to be superior to the other two. Each phratry is further sub-divided into a number of exogamous intiperlu (surnames). Some of the intiperlu are Kumbha, Mogili, Katta, Devara, Sreerama, Palaparthi, Meda etc, Endogamy at community level and exogamy at phratry and intiperlu lever are observed.
        </p>
