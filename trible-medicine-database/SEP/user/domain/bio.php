        <h2>22. Nayak Tribe</h2>
        <p>
        Nayaks are found in the districts of Srikakulam, Vizianagaram, Visakhapatnam, East Godavari, West Godavari and Khammam. The Navales claim that their forefathers served in the armies of the kings of Vizianagaram and hence they were called Nayaks (Chiefs).
        Their mother tongue is Telugu. Nayak community is divided into various exogamous septs called intiperlu. Monogamy is prevalent among Nayaks and polygyny is also practised. 
        They are patriarchal. Levirate and sororate are in practice. Divorce and remarriages are permitted.
        Nayaks subsist on agriculture and they practice podu cultivation also. Most of them are agricultural labourers. They supplement their income by collecting minor forest produce also.
