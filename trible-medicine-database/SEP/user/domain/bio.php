        <p>
            In Andhra Pradesh, Andha are living in the hilly tracts of Adilabad district.The Andh is divided into two endogamous groups viz., Vertail and Khaitali. The people belonging to Vartali section are considered superior to Khaltali section and as such they do not inter-marry. Andh tribe is further divided into a number of exogamous septs or surnames (Intiperiu) known as Adman. Each Adman is an exogamous unit. The marriage by negotiations le common among Andhs but marriage by intrusion is also prevalent. Widow remarriages are permitted among Andha. But the Widow is not permitted to marry her deceased husbands brother or any member of his sept. Divorce is permissible. They speak Marati as their mother tongue. Andhs do not consume beef.
