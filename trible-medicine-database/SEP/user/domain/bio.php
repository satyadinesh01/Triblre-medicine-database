        <h2>5. Gadabas Tribe</h2>
        <p>Gadabas are predominantly found in tribal areas of Srikakulam, Vizianagaram and Visakhapatnam districts. The Gadaba tribe is divided into different sub divisions viz, Bodo or Gutob, Katheri, Kollari etc., Each sub division which is endogamous is divided into various exogamous clans. The modes of acquiring mates among Gadabas are marriage by negotiation, by mutual love and elopement, by capture and by service. Family is nuclear. Widow re-marriage and divorce are permitted.
At present Gadabas are cultivators and agricultural labourers. Those who inhabit the hilly areas practice shifting cultivation and they cultivate Ragi, Red gram, Niger in their Podu lands. They collect Non-Timber Forest Produce for household consumption and sale.
They worship Sankudevudu, Peddadevudu, Moda Kondamma, Jakaridevatha, Ippapolamma etc., and they celebrate festivals like, Eetelapanduga, Ashadapanduga (Korrakotha), Kothamasa and Maridamma Panduga. In addition to the above festivals, they worship the spirits of their ancestors.
Gadabas have their own traditional council and traditional village headman known as "Naiko". His office is hereditory. He is assisted by 'Challan', (messenger) and Barika' (Village servant). In the fields of religious activities, Desari' or Village Pujari officiates all the religious ceremonies. Gadabas are recognized as Primitive Tribal Group in A.P.
</p>
