own dialect called Kui or Kuvi. But the Khonds living in Srikakulam are equally proficient in Telugu and Khonds in Araku and other bordering areas are multilingual. The Khonds have a tribal council usually consisting of four or five members headed by a man called Havanta, whose office is hereditary. The members of the council are selected. The main functions of the council are settlement of disputes on marriage, land and other property.
