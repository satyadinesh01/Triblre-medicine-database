        <h2>14. Konda_Reddis Tribe</h2>
        <p>Konda Reddis are inhabiting on the banks situated on either side of river Godavari in the hilly and forest tracts of East and West Godavari and Khammam districts of Andhra Pradesh. 
They are recognized as Primitive Tribal Group. Konda Reddy tribe is divided into exogamous septs for regulating matrimonial relations. Like other Telugu speaking people their surnames are pre-fixed to individual names. Generally, each sept is exogamous but certain septs are considered as brother septs and marriage alliances with brother septs (agnate relations) are prohibited. They are primarily shifting cultivators and largely depend on flora and fauna of forest for their livelihood. They eat a variety of tubers, roots, leaves, wild fruits etc., They collect and sell non timber forest produce like tamarind, adda leaves, myrobolan, broom sticks etc., to supplement their meagre income. They cultivate largely jowar, which is their staple food. They also grow chodi, red gram, bajra, beans, paddy and pulses. They eat pork but do not consume beef.
