        <h2>2. Bagata Tribe</h2>
        <p>Bagata is one of the numerically preponderant and ethnically significant tribes of Andhra Pradesh and distributed predominantly in scheduled areas of Visakhapatnam district. Majority of the former Mutadars and traditional village headmen in the tribal areas of Visakhapatnam district belong to this tribe. They occupy highest rung in the local social hierarchical ladder. 
They prohibit eating of beef and pork. Bagata tribe is divided into a number of unilateral agnatic kingroups called "Gothrams" or "Vamsams" such as Korra (Sun), Killo or Bagh (Tiger), Gollari (Monkey), Pangi (Kite) etc., and the members of each gothram presume that they have descended from a common ancestor. These gothrams are further divided into a number of surnames (Intiperlu).
The socially approved modes of acquiring mates include marriage by negotiation, marriage by capture, marriage by mutual love and elopement and marriage by service. Out of these marriage by negotiation is widely practised and marriage is performed in the groom's house. The custom of paying bride price to the bride's parents is in vogue in this community. Monogámy is common form of marriage while polygamy is rarely practised. Levirate and sororate are in vogue. Widow rémarriage is permitted. Divorce is socially accepted.
